namespace PureComponents.NicePanel
{
	/// <summary><P>Specifies the border style for a <see cref="T:PureComponents.NicePanel.NicePanel" /> control.</P></summary>
	/// <remarks><P>You can use the <see cref="P:PureComponents.NicePanel.ContainerStyle.BorderColor" /> property to set color of border.</P></remarks>
	public enum BorderStyle
	{
		/// <summary><P><a name="3" />The <see cref="T:pureComponents.NicePanel.NicePanel" /> is displayed without border.</P></summary>
		None,
		/// <summary><P><a name="1" />The <see cref="T:pureComponents.NicePanel.NicePanel" /> is displayed with dotted line border.</P></summary>
		Dot,
		/// <summary><P><a name="4" />The <see cref="T:pureComponents.NicePanel.NicePanel" /> is displayed with single pixel width line border.</P></summary>
		Solid,
		/// <summary><P><a name="2" />The <see cref="T:pureComponents.NicePanel.NicePanel" /> is displayed with two pixel line border.</P></summary>
		Double
	}
}
